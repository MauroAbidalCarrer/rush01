#include "header.h"

void	print_game(t_game g)
{
	int	x;
	int	y;

	y = -1;
	while (++y < 4)
	{
		x = -1;
		while (++x < 4)
		{
			ft_putchar('0' + g.grid[x][y]);
			if (x != 3)
				ft_putchar(' ');
		}
		ft_putchar('\n');
	}
}
